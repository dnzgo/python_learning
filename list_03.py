places = ['Berlin', 'Stuttgart', 'Istanbul', 'Paris', 'Amsterdam']
print(places)

print(sorted(places))
print(places)

print(sorted(places, reverse= True)) # to sort reverse
print(places)

places.reverse() # reverse original list permanently
print(places)

places.reverse() # reverse again to go back to original
print(places)

places.sort() # sort alphabetical order
print(places)

places.sort(reverse= True)
print(places)

print(len(places)) # size of list

